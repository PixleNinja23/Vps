        echo -ne "${B_WHITE}в–Ҳ${NC}"
        sleep 0.03
    done
    echo -e "${B_RED}]${NC}"
    sleep 0.5
    echo -e "${B_WHITE}Loaded Successfully!${NC}"
    sleep 1
}

# --- Utility Functions ---
pause() {
    echo ""
