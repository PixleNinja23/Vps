# --- Animation Function ---
# Usage: animate_text "Text to animate" delay_time
animate_text() {
    text="$1"
    delay="${2:-0.01}"
    for (( i=0; i<${#text}; i++ )); do
        echo -n "${text:$i:1}"
        sleep "$delay"
    done
    echo ""
}

# --- Loading Animation ---
loading_bar() {
    clear
